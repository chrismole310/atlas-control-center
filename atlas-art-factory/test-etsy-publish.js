'use strict';

/**
 * End-to-end test: publish a listing to Etsy using kontext-test mockup images.
 *
 * Prerequisites:
 *   1. Set ETSY_API_KEY, ETSY_SHOP_ID in .env
 *   2. Run: node etsy-oauth-setup.js   (fills ETSY_ACCESS_TOKEN + ETSY_REFRESH_TOKEN)
 *   3. Run: node test-etsy-publish.js
 */

require('dotenv').config();

const path = require('path');
const fs = require('fs');
const Anthropic = require('@anthropic-ai/sdk');
const EtsyUploader = require('./engines/distribution/uploaders/etsy');

const STORAGE = path.join(__dirname, 'storage');
const MOCKUPS_DIR = path.join(STORAGE, 'mockups');
const PACKAGES_DIR = path.join(STORAGE, 'packages');

// ── Credential check ──────────────────────────────────────────────────────────
function checkCredentials() {
  const required = ['ETSY_API_KEY', 'ETSY_SHOP_ID', 'ETSY_ACCESS_TOKEN'];
  const missing = required.filter(k => !process.env[k]);
  if (missing.length > 0) {
    console.error('\n❌  Missing Etsy credentials in .env:');
    missing.forEach(k => console.error(`     ${k}=<not set>`));
    console.error('\n   Steps:');
    console.error('     1. Add ETSY_API_KEY + ETSY_SHOP_ID to atlas-art-factory/.env');
    console.error('     2. node etsy-oauth-setup.js   (fills access + refresh tokens)');
    console.error('     3. node test-etsy-publish.js\n');
    process.exit(1);
  }
}

// ── Resolve test assets ───────────────────────────────────────────────────────
function resolveTestMockups() {
  if (!fs.existsSync(MOCKUPS_DIR)) {
    console.error(`❌  Mockups directory not found: ${MOCKUPS_DIR}`);
    process.exit(1);
  }

  // Prefer kontext-test_* images (generated by Kontext AI room scenes)
  let mockups = fs.readdirSync(MOCKUPS_DIR)
    .filter(f => f.startsWith('kontext-test_') && f.endsWith('.png'))
    .sort()
    .map(f => path.join(MOCKUPS_DIR, f));

  // Fallback to any test_* images
  if (mockups.length === 0) {
    mockups = fs.readdirSync(MOCKUPS_DIR)
      .filter(f => f.startsWith('test_') && f.endsWith('.png'))
      .sort()
      .map(f => path.join(MOCKUPS_DIR, f));
  }

  if (mockups.length === 0) {
    console.error('❌  No test mockup images found in storage/mockups/');
    console.error('   Expected files like: kontext-test_living-room.png');
    process.exit(1);
  }

  console.log(`\n📸  Found ${mockups.length} mockup image(s):`);
  mockups.forEach(p => console.log(`    ${path.basename(p)}`));

  return mockups;
}

function resolveTestZip() {
  // Look for any .zip in packages (prefer 1.zip as a known test file)
  const candidates = ['1.zip'];
  for (const name of candidates) {
    const p = path.join(PACKAGES_DIR, name);
    if (fs.existsSync(p)) {
      console.log(`\n📦  Using ZIP: ${path.basename(p)}`);
      return p;
    }
  }

  // Accept any .zip
  if (fs.existsSync(PACKAGES_DIR)) {
    const zips = fs.readdirSync(PACKAGES_DIR).filter(f => f.endsWith('.zip'));
    if (zips.length > 0) {
      const p = path.join(PACKAGES_DIR, zips[0]);
      console.log(`\n📦  Using ZIP: ${path.basename(p)}`);
      return p;
    }
  }

  console.log('\n⚠️   No ZIP found in storage/packages/ — listing will publish without digital file');
  return null;
}

// ── Generate SEO content via Claude ──────────────────────────────────────────
async function generateListingContent() {
  if (!process.env.ANTHROPIC_API_KEY) {
    console.log('\n💡  No ANTHROPIC_API_KEY — using placeholder SEO content');
    return {
      title: 'Botanical Art Print | Minimalist Watercolor Wall Art | Digital Download',
      description: [
        'Transform your space with this stunning botanical art print. Perfect for living rooms, bedrooms, and home offices.',
        '',
        '✨ WHAT YOU GET',
        '• 5 high-resolution print formats (4×6, 5×7, 8×10, 11×14, 16×20)',
        '• Instant digital download — no waiting!',
        '• Print at home or at your local print shop',
        '',
        '🎨 ABOUT THIS PRINT',
        'Minimalist watercolor botanical illustration in soft, natural tones. Pairs beautifully with modern, Scandinavian, or boho interiors.',
        '',
        '📐 PRINT SIZES INCLUDED',
        '4×6 | 5×7 | 8×10 | 11×14 | 16×20 inches (300 DPI)',
        '',
        '⚡ INSTANT DOWNLOAD — Files available immediately after purchase.',
        '',
        '💌 Questions? Message me anytime — I respond within 24 hours.',
      ].join('\n'),
      tags: [
        'botanical print', 'wall art', 'digital download', 'printable art',
        'watercolor print', 'minimalist art', 'home decor', 'instant download',
        'bedroom decor', 'living room art', 'botanical decor', 'nature print',
        'gallery wall',
      ],
    };
  }

  const anthropic = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

  console.log('\n🤖  Generating SEO content with Claude...');

  const msg = await anthropic.messages.create({
    model: 'claude-haiku-4-5-20251001',
    max_tokens: 800,
    messages: [{
      role: 'user',
      content: `You are an Etsy SEO expert. Generate a listing for a digital art print.

Art theme: Botanical minimalist watercolor — soft greens and earth tones, suitable for modern home decor.
Product: Instant digital download, 5 print sizes (4×6 to 16×20 at 300 DPI), includes room mockups.
Price: $4.99

Return JSON with:
- title: under 140 chars, keyword-rich Etsy title
- description: compelling multi-paragraph description with emojis, bullet points, size list
- tags: array of exactly 13 Etsy tags (2-3 words each, no duplicates)

JSON only, no markdown.`,
    }],
  });

  const text = msg.content[0].text.trim();
  const jsonText = text.startsWith('{') ? text : text.slice(text.indexOf('{'));
  return JSON.parse(jsonText);
}

// ── Main ──────────────────────────────────────────────────────────────────────
async function main() {
  console.log('\n🚀  Etsy publish test — atlas-art-factory\n');
  console.log('='.repeat(50));

  checkCredentials();

  const mockupPaths = resolveTestMockups();
  const zipPath = resolveTestZip();
  const content = await generateListingContent();

  console.log(`\n📝  Title: ${content.title}`);
  console.log(`🏷️   Tags:  ${content.tags.join(', ')}`);

  const uploader = new EtsyUploader();

  console.log('\n⏳  Publishing to Etsy...\n');

  const result = await uploader.upload({
    title: content.title,
    description: content.description,
    price: 4.99,
    tags: content.tags,
    mockupPaths,
    zipPath,
  });

  console.log('\n' + '='.repeat(50));
  console.log('✅  Published!\n');
  console.log(`   Platform:   ${result.platform}`);
  console.log(`   Listing ID: ${result.platformListingId}`);
  console.log(`   URL:        ${result.listingUrl}`);
  console.log('\n   Visit the URL to verify: 5 room photos, Digital Download badge, correct price + tags.');
  console.log('='.repeat(50) + '\n');
}

main().catch((err) => {
  const detail = err.response?.data ? JSON.stringify(err.response.data, null, 2) : err.message;
  console.error('\n❌  Test failed:\n', detail);
  process.exit(1);
});
